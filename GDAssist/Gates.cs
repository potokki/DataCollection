using System;

namespace GDAssist
{
    public class Gates
    {

        public void SSS(int j)
        {
            int i = j;
            i += 3;
            if (i > 3)
            {
                throw new InvalidTimeZoneException();
            }
            else
            {
                throw new NullReferenceException();
            }
        }
    }
}
